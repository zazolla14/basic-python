#OPERASI LOGIKA/LOGIC OPERATION = operasi yang digunakan pada BOOLEAN yang akan menghasilkan keluaran BOOLEAN (TRUE/FALSE)
#penggunaan operasi logika yaitu = and,or,not,

print(True and True)
print(True and False)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)